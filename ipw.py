# Program to showcase working off different types of bitwise operators
num1 = 10
num2 = 4 
# Using AND operator
print("num1 & num2 =", num1 & num2)
# Using OR operator
print("\n num1 | num2", num1 | num2)
# Using NOT operator
print("\n num1 ", ~num1 ) 
# Using XOR operator
print("\n num1 ^ num2", num1 ^ num2)
